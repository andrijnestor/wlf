#include "libft.h"
#include <stdlib.h>

char	*ft_strrmap(char *s, char (*f)(char))
{
	int		n;

	if (s && f)
	{
		n = 0;
		while (s[n] != '\0')
		{
			s[n] = (*f)(s[n]);
			n++;
		}
	}
	return (s);
}
