#include <math.h>
#include <stdio.h>
#define RAD(x) (x * M_PI / 180.0)

int		main(void)
{
	int		i;

	i = 0;
	while (i != 360)
	{
		printf("i: %d tan: %f\n", i, tan(RAD(i) + 0.0000f));
		i++;
	}
	return (0);
}
